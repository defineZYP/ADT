class Decoder(nn.Module):
    def __init__(self, num_layers, hidden_units, num_heads, dropout):
        super(Decoder, self).__init__()
        self.num_layers = num_layers
        self.hidden_units = hidden_units
        self.num_heads = num_heads
        self.dropout = dropout

        self.decoder_layers = torch.nn.ModuleList()

        for _ in range(num_layers):
            # decoder block
            self.decoder_layers.append(DecoderLayer(hidden_units, num_heads, dropout))

    def forward(self, seqs, encode_outputs, timeline_mask, attention_mask, src_masks):
        decoder_layer_output = []
        for i in range(len(self.decoder_layers)):
            seqs, _, _ = self.decoder_layers[i](seqs, encode_outputs, timeline_mask=timeline_mask, slf_attn_mask=attention_mask, enc_attn_mask=src_masks)
            decoder_layer_output.append(seqs)
        decoder_layer_output.reverse()
        return seqs, decoder_layer_output